package com.assosiation;

public class Aggri_Gation {
	String name;
	String city;
	int id;

	//constuctor
	public Aggri_Gation(String name, String city, int id) {
		this.name = name;
		this.city = city;
		this.id = id;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public void setId(int id) {
		this.id = id;
	}

}
