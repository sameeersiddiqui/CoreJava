package com.cantrolstatement;

public class looping {
	public static void main(String[] args) {
		// for loop printing 1to10

		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		// while loop

		int j = 1;
		while (j <= 10) {

			System.out.println(j);
			j++;
		} // do while
		int f = 1;
		do {

			System.out.println(f);
			f++;
		} while (f <= 10);

	}

}
